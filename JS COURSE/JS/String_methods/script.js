
let username = "Hello";

console.log(username.charAt(0)); // Gives the character of the specified index passed as argument

console.log(username.indexOf("H")); // Returns the first occurence of the character 

console.log(username.length); // Returns the Length of the String

console.log(username.toUpperCase());

console.log(username.toLowerCase());

console.log(username.repeat(3)); // The given string is repeated as many times as mentioned in argument

console.log(username.startsWith(" ")); // Returns boolean if the string starts with the argument passed as string



